/** Interfaccia che descrive il contratto di una superficie. */
public interface Superficie {
    
    /**
     * Restituisce l'area totale della superficie.
     * 
     * @return l'area
     */
    int superficie();

    /**
     * Restituisce il costo della superficie.
     * 
     * @return il costo
     */
    int costo();
    
}